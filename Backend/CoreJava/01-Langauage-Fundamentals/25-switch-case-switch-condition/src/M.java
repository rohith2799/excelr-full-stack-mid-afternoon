class M
{
	public static void main(String[] args) 
	{
		char i = 'a';
		switch(i)
		{
			case 'a':
				System.out.println("from case 'a'");
				break;
			case 'b':
				System.out.println("from case 'b'");
				break;
			case 'c':
				System.out.println("from case 'c'");
				break;
		}
	}
}
//* According to switch case char case must be the same or else output will get error.