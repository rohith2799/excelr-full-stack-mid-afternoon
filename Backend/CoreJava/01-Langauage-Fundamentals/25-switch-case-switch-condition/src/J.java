class J
{
	public static void main(String[] args) 
	{
		int i = 10;
		switch(i)
		{
			default:
				System.out.println("from default 2");
			case 2:
				System.out.println("from case 2");
				break;
			case 3:
				System.out.println("from case 3");
				break;
		}
	}
}
